#include <unistd.h>

void	ft_print_comb(void)
{
	char	n3;
	char	n2;
	char	n1;

	n3 = '0';
	while (n3 <= '7')
	{
		n2 = n3 + 1;
		while (n2 <= '8')
		{
			n1 = n2 + 1;
			while (n1 <= '9')
			{
				write(1, &n3, 1);
				write(1, &n2, 1);
				write(1, &n1, 1);
				if (!(n3 == '7' && n2 == '8' && n1 == '9'))
					write(1, ", ", 2);
				n1++;
			}
			n2++;
		}
		n3++;
	}
}

